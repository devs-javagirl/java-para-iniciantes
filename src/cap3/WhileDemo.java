package cap3;

// Demonstra o laço while.
class WhileDemo {
    public static void main(String args[]) {
        char ch = 'a'; // inicializa ch com 'a'
        while(ch <= 'z') { // condição para o laço
            System.out.print(ch); // exibe ch
            ch++; // incrementa ch
        }
    }
}
